package com.epam.loops;

public class MultiplicationTable {

    public void printTable(int n) {
        System.out.printf("1 x %d = %d%n", n, n);
        System.out.printf("2 x %d = %d%n", n, 2 * n);
        System.out.printf("3 x %d = %d%n", n, 3 * n);
        System.out.printf("4 x %d = %d%n", n, 4 * n);
        System.out.printf("5 x %d = %d%n", n, 5 * n);
        System.out.printf("6 x %d = %d%n", n, 6 * n);
        System.out.printf("7 x %d = %d%n", n, 7 * n);
        System.out.printf("8 x %d = %d%n", n, 8 * n);
        System.out.printf("9 x %d = %d%n", n, 9 * n);
        System.out.printf("10 x %d = %d%n", n, 10 * n);
    }
}
